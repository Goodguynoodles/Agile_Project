<h1 class="font-chivo">Lantern Festival</h1>
<p class="font-chivo">de Chinese nieuwjaarsvakantie bereikt zijn hoogtepunt met de Yuan Xiao (元宵节 / yuán xiāo jié) of Lantaarnfestival.</p>
<br>
<p class="font-chivo">Begonnen meer dan 2000 jaar geleden, heeft het festival vele betekenissen ontwikkeld. Het viert familiereünies en de samenleving. Het bevat oude spirituele tradities. Sommigen noemen dit ook wel de 'echte' Chinese Valentijnsdag.</p>
<p class="font-chivo">De vele activiteiten omvatten maankijken, lantaarns aansteken, raadsels, leeuwendansen en het eten van rijstballen. Volgens de maankalender vindt het festival plaats op 15 januari. Data van voorbije en toekomstige Lantaarnfestivals met gebruikmaking van de Gregoriaanse kalender zijn onder meer:</p>

