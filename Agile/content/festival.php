<h1 class="font-chivo">Culturele betekenis van het lantaarnfestival</h1>
<br>
<p class="font-chivo">Het Lentefestival is een tijd gereserveerd voor gezinnen. Zo is er het reüniediner op oudejaarsavond, bezoek (拜年 / bài nián) aan schoonouders op de 2e dag en daarna buren. Winkels gaan op de 5e weer open en de samenleving wordt in principe weer normaal.</p>
<br>
<img src="./img/chinese-festival2.jpg" class="border-yuanxiao" style="width:1000px;height:500px;">
<br>
<br>
<p class="font-chivo">Maar op de 15e gaat iedereen - ongeacht leeftijd of geslacht - de straat op om feest te vieren. Hoewel het Lantaarnfestival reünies symboliseert (daarover later meer), is het ook een tijd van gezelligheid en vrijheid.</p>
<br>
<p class="font-chivo">In het oude China mochten vrouwen het huis meestal niet uit. Maar deze nacht kunnen ze vrij rondlopen, lantaarns aansteken, spelletjes spelen en interactie hebben met mannen. De wilde en romantische verhalen zijn waarom sommigen zeggen dat het Lantaarnfestival de echte Chinese Valentijnsdag is, in plaats van Qixi (七夕).</p>
<br>
<p class="font-chivo">Aan een serieuzere kant heeft het Lantaarnfestival ook religieuze aspecten. Het was belangrijk in het oude Chinese heidendom, maar ook in het moderne boeddhisme en culturen van etnische minderheden.</p>
